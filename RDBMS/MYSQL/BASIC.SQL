-------------------------------- DELETE --------------------------------
-- Delete用于删除表中的行,表的结构,属性,索引完整
delete from t;  
delete * from t; 

-- Drop：用于删除表,表的结构,属性,索引也会被删除
drop table t;

-- Truncate：用于删除表内的数据,仅删除表内的数据,不删除表本身
truncate table t;

 

-------------------------------- OTHER --------------------------------
-- 查看表结构
desc tabl_name;

-- 命令行手动控制事务提交
begin;
select * from t;
commit;

-- 命令行手动控制事务提交回滚
begin;
select * from t;
rollback;
